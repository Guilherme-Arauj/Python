list = [-10, -8, 0, 1, 2, 5, -2,-4]

#a) Biggest number on the list
biggest = int(max(list))
print(f"a) {biggest}")
print("-----------------")

#b) Smallest number on the list
smallest = int(min(list))
print(f"b) {smallest}")
print("-----------------")

#e) Average
media = sum(list) / len(list) 
print("e)", end = " ")
print(f"Average: {media:.2f}")
